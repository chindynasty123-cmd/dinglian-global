function render(lang) {
  const data = localizedData[lang];
  storyTags.innerHTML = data.storyTags.map((t) => `<span>${t}</span>`).join("");
  trustGrid.innerHTML = data.trust
    .map(
      (t) =>
        `<article class="trust-card reveal"><strong>${t[0]}</strong><span>${t[1]}</span></article>`,
    )
    .join("");
  industriesGrid.innerHTML = data.industries
    .map(
      (i) =>
        `<article class="industry-card reveal"><div class="icon">${i[0]}</div><h3>${i[1]}</h3><p class="muted">${i[2]}</p></article>`,
    )
    .join("");
  solutionGrid.innerHTML = data.solutions
    .map(
      (s, i) =>
        `<article class="solution-card reveal"><div class="icon">${String(i + 1).padStart(2, "0")}</div><h3>${s}</h3></article>`,
    )
    .join("");
  qualityList.innerHTML = data.quality
    .map((q) => `<div class="quality-item">${q}</div>`)
    .join("");
  baseList.innerHTML = data.base.map((b) => `<span>${b}</span>`).join("");
}
function observeReveals() {
  const io = new IntersectionObserver(
    (es) =>
      es.forEach((e) => {
        if (e.isIntersecting) e.target.classList.add("visible");
      }),
    { threshold: 0.12 },
  );
  document.querySelectorAll(".reveal").forEach((el) => io.observe(el));
}
function setLang(lang) {
  render(lang);
  document.documentElement.lang = lang === "zh" ? "zh-Hant" : "en";
  document
    .querySelectorAll("[data-i18n]")
    .forEach(
      (el) =>
        (el.innerHTML = translations[lang][el.dataset.i18n] || el.innerHTML),
    );
  document
    .querySelectorAll("[data-i18n-html]")
    .forEach(
      (el) =>
        (el.innerHTML =
          translations[lang][el.dataset.i18nHtml] || el.innerHTML),
    );
  document
    .querySelectorAll("[data-placeholder]")
    .forEach(
      (el) =>
        (el.placeholder = translations[lang][el.dataset.placeholder] || ""),
    );
  document
    .querySelectorAll(".lang")
    .forEach((b) => b.classList.toggle("active", b.dataset.lang === lang));
  localStorage.setItem("dinglianLang", lang);
  observeReveals();
}
